"""
Абстрактный интерфейс провайдера данных.

Любая реализация (LocalProvider — SQLite + JSON, BitrixProvider — Битрикс24 API)
обязана возвращать данные в одном и том же формате. Бот вызывает только
эти три метода и не знает, откуда пришла информация.
"""
from abc import ABC, abstractmethod


class DataProvider(ABC):
    """Контракт провайдера данных для ВК-бота фитнес-клуба «Альбатрос»."""

    @abstractmethod
    def find_member(self, card_id: str) -> dict | None:
        """
        Найти абонемент по номеру карты.

        Возвращает:
            dict со структурой:
                {
                    "card_id":       str,   # «ALB-001»
                    "name":          str,   # «Иванова Мария Петровна»
                    "sessions_left": int,   # 12
                    "valid_until":   str,   # «2026-06-15» (ISO 8601)
                }
            либо None, если карта не найдена или источник недоступен.
        """
        ...

    @abstractmethod
    def list_trainers(self) -> list[dict]:
        """
        Получить список тренеров.

        Возвращает список словарей со структурой:
            {
                "name":           str,   # «Мария Орлова»
                "specialization": str,   # «Йога, Медитация»
                "description":    str,   # развёрнутое описание
            }
        Если источник недоступен — возвращает пустой список.
        """
        ...

    @abstractmethod
    def list_schedule(self) -> list[dict]:
        """
        Получить расписание занятий.

        Возвращает список словарей со структурой:
            {
                "day":       str,   # «Пн»…«Вс»
                "time":      str,   # «09:00»
                "direction": str,   # «Йога»
                "trainer":   str,   # «Мария Орлова»
                "hall":      str,   # «Зал 1»
            }
        Если источник недоступен — возвращает пустой список.
        """
        ...

    def health_check(self) -> bool:
        """
        Проверить доступность источника данных.
        По умолчанию считает любой провайдер живым; конкретные реализации
        могут переопределить (например, BitrixProvider пингует API).
        """
        return True
